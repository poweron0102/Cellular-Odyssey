    oc.route = [(0, 13.5 * Tile_size), (10.5 * Tile_size, 13.5 * Tile_size), (10.5 * Tile_size, 23.5 * Tile_size)]
    in_game.sprite_handler.add(oc)
    oc = MovingSprite(in_game, 'ordinary-cell', 0, 0, scale=0.55, shift=0.3)
    oc.route = [(13.5 * Tile_size, 23.5 * Tile_size), (13.5 * Tile_size, 13.5 * Tile_size), (23.5 * Tile_size, 13.5 * Tile_size)]
    in_game.sprite_handler.add(oc)
